from nltk.stem import WordNetLemmatizer\


wordnet_lemmatizer = WordNetLemmatizer()

def lemma_text(pos_data):


    lemma_rew = " "
    for word, pos in pos_data:
        if not pos:
            lemma = word
            lemma_rew = lemma_rew + " " + lemma
        else:
            lemma = wordnet_lemmatizer.lemmatize(word, pos=pos)
            lemma_rew = lemma_rew + " " + lemma
    return lemma_rew

